const SEVERITY_COLORS: Record<string, string> = {
  High: '#e74c3c',
  Medium: '#f39c12',
  Low: '#5fb04b',
};

const CATEGORY_LABELS: Record<string, Record<string, string>> = {
  en: { vitals: 'Core Vitals', conversion: 'Conversion Architecture', seo: 'SEO Surface' },
  fr: { vitals: 'Métriques principales', conversion: 'Architecture de conversion', seo: 'Surface SEO' },
  de: { vitals: 'Kern-Metriken', conversion: 'Konversionsarchitektur', seo: 'SEO-Oberfläche' },
  es: { vitals: 'Métricas principales', conversion: 'Arquitectura de conversión', seo: 'Superficie SEO' },
  ja: { vitals: 'コアバイタル', conversion: 'コンバージョン設計', seo: 'SEO表面積' },
};

function scoreColor(score: number): string {
  if (score >= 70) return '#5fb04b';
  if (score >= 50) return '#f39c12';
  return '#e74c3c';
}

function findingCard(finding: ScanFinding, L: Record<string, string>): string {
  const color = SEVERITY_COLORS[finding.severity] ?? '#888';
  return `
    <div style="border-left:4px solid ${color};background:#f8f9fb;border-radius:6px;padding:14px 16px;margin-bottom:10px;">
      <div style="display:flex;justify-content:space-between;align-items:flex-start;gap:12px;">
        <div>
          <div style="font-weight:600;font-size:13px;color:#1a1a1a;margin-bottom:4px;">${escHtml(finding.title)}</div>
          <div style="font-size:12px;color:#555;">${escHtml(finding.description)}</div>
        </div>
        <div style="text-align:right;flex-shrink:0;">
          <div style="font-size:11px;font-weight:700;color:${color};">${escHtml(L.severity)}: ${escHtml(finding.severity)}</div>
          <div style="font-size:12px;color:#3264b4;font-weight:600;margin-top:3px;">+$${finding.impact.toLocaleString('en-CA')}</div>
        </div>
      </div>
    </div>`;
}

function escHtml(s: string): string {
  return s.replace(/&/g, '&amp;').replace(/</g, '&lt;').replace(/>/g, '&gt;').replace(/"/g, '&quot;');
}

export async function generateProfitBlueprint(result: ScanResult): Promise<void> {
  const lang = result.detectedLanguage in LOCALE_LABELS ? result.detectedLanguage : 'en';
  const L = LOCALE_LABELS[lang];
  const CAT = CATEGORY_LABELS[lang] ?? CATEGORY_LABELS['en'];

  const sev = result.severity;
  const sevColor = SEVERITY_COLORS[sev] ?? '#888';
  const dateStr = result.timestamp.toLocaleDateString(lang, { year: 'numeric', month: 'long', day: 'numeric' });

  const groupedFindings: Record<string, ScanFinding[]> = { vitals: [], conversion: [], seo: [] };
  result.findings.forEach((f) => groupedFindings[f.category]?.push(f));

  const findingsSections = Object.entries(groupedFindings)
    .filter(([, items]) => items.length > 0)
    .map(([cat, items]) => `
      <h3 style="font-size:13px;font-weight:700;color:#333;margin:18px 0 8px;">${escHtml(CAT[cat] ?? cat)}</h3>
      ${items.map((f) => findingCard(f, L)).join('')}
    `).join('');

  const html = `<!DOCTYPE html>
<html lang="${escHtml(lang)}">
<head>
  <meta charset="UTF-8" />
  <title>${escHtml(L.title)}</title>
  <style>
    * { box-sizing: border-box; margin: 0; padding: 0; }
    body { font-family: Helvetica, Arial, sans-serif; color: #1a1a1a; background: #fff; }
    .page { max-width: 800px; margin: 0 auto; padding: 0 0 40px; }
    @media print {
      body { -webkit-print-color-adjust: exact; print-color-adjust: exact; }
    }
  </style>
</head>
<body>
<div class="page">
  <div style="background:#0f1a2d;padding:36px 32px 28px;border-left:4px solid #4A9EFF;">
    <div style="font-size:26px;font-weight:700;color:#fff;margin-bottom:6px;">${escHtml(L.title)}</div>
    <div style="font-size:13px;color:#96b4dc;margin-bottom:10px;">${escHtml(L.subtitle)}</div>
    <div style="display:flex;justify-content:space-between;font-size:11px;color:#7896be;">
      <span>${escHtml(result.url)}</span>
      <span>${escHtml(dateStr)}</span>
    </div>
  </div>

  <div style="padding:28px 32px 0;">
    <h2 style="font-size:15px;font-weight:700;color:#111;margin-bottom:8px;">${escHtml(L.summary)}</h2>
    <hr style="border:none;border-top:1.5px solid #4A9EFF;margin-bottom:16px;" />

    <div style="border:1.5px solid ${sevColor};border-radius:6px;background:#f8f9fb;padding:16px 20px;margin-bottom:18px;">
      <div style="font-size:11px;color:#555;margin-bottom:6px;">${escHtml(L.recovery)}</div>
      <div style="display:flex;justify-content:space-between;align-items:center;">
        <div style="font-size:26px;font-weight:700;color:${sevColor};">$${result.totalLeakEstimate.toLocaleString('en-CA')}</div>
        <div style="font-size:12px;font-weight:700;color:${sevColor};">${escHtml(L.severity)}: ${escHtml(sev)}</div>
      </div>
    </div>

    <div style="display:flex;gap:12px;margin-bottom:24px;">
      ${[
        { label: L.vitals, score: result.scores.vitals },
        { label: L.conversion, score: result.scores.conversion },
        { label: L.seo, score: result.scores.seo },
      ].map(({ label, score }) => `
        <div style="flex:1;background:#f8f9fb;border:1px solid #e0e0e0;border-radius:6px;padding:12px;text-align:center;">
          <div style="font-size:10px;color:#666;margin-bottom:6px;">${escHtml(label)}</div>
          <div style="font-size:22px;font-weight:700;color:${scoreColor(score)};">${score}</div>
        </div>
      `).join('')}
    </div>

    <h2 style="font-size:15px;font-weight:700;color:#111;margin-bottom:8px;">${escHtml(L.findings)}</h2>
    <hr style="border:none;border-top:1.5px solid #4A9EFF;margin-bottom:12px;" />
    ${findingsSections}

    <div style="margin-top:32px;border-top:1px solid #ddd;padding-top:10px;display:flex;justify-content:space-between;font-size:10px;color:#999;">
      <span>${escHtml(L.generatedBy)}</span>
      <span>${escHtml(L.confidential)}</span>
    </div>
  </div>
</div>
</body>
</html>`;

  const blob = new Blob([html], { type: 'text/html;charset=utf-8' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `colony-os-profit-blueprint-${Date.now()}.html`;
  a.click();
  setTimeout(() => URL.revokeObjectURL(url), 5000);
}
